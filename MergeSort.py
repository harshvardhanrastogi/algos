import math

class MergeSort:

    def __init__(self):
        pass
    
    def mergeSort(self, arr, p: int, r: int):
        if p < r:
            q = math.floor((p + r) / 2)
            self.mergeSort(arr, p, q)
            self.mergeSort(arr, q + 1, r)
            self.merge(arr, p, q, r)

    def merge(self, arr, p: int, q: int, r: int):
        n1 = q - p + 1
        n2 = r - q
        left = [None]*(n1 + 1)
        right = [None]*(n2 + 1)

        for i in range(n1):
            left[i] = arr[p + i]
        for j in range(n2):
            right[j] = arr[q + j + 1];
        left[n1] = math.inf
        right[n2] = math.inf
        i = 0
        j = 0
        for k in range(p, r + 1):
            if left[i] <= right[j]:
                arr[k] = left[i]
                i = i + 1
            else:
                arr[k] = right[j]
                j = j + 1


